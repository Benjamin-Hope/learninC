#include <stdio.h>
#include <math.h>

int main()
{
    printf("Hello, World!\n");

    // Setting up mathematical operations
    double A = sqrt(2);    // Square root function
    double B = pow(2, 3);  // Power function = 2^3 = 8
    int C = round(2.3);    // Round function = 2
    int D = ceil(2.3);     // Ceil function = 3
    int E = floor(2.3);    // Floor function = 2
    double F = fabs(-2.3); // Absolute function = 2.3
    double G = log(2);     // Logarithm function = 0.693147
    double H = sin(2);     // Sine function = 0.909297
    double I = cos(2);     // Cosine function = -0.416147
    double J = tan(2);     // Tangent function = -2.18504

    printf("\n%lf", A);

    return 0;
}